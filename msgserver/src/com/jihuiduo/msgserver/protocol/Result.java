package com.jihuiduo.msgserver.protocol;

/**
 * 服务器回执
 *
 */
public class Result {
	/**
	 * 序列号
	 */
	private String seq;
	/**
	 * 目标用户user_id
	 */
	private String user_id;
	
	private Integer rcode;
	
	private String rmsg;
	
	private Object data;
	
	public Result() {
		
	}

	public Result(String seq, String user_id, Integer rcode, String rmsg,
			Object data) {
		this.seq = seq;
		this.user_id = user_id;
		this.rcode = rcode;
		this.rmsg = rmsg;
		this.data = data;
	}

	public String getSeq() {
		return seq;
	}

	public void setSeq(String seq) {
		this.seq = seq;
	}

	public String getUser_id() {
		return user_id;
	}

	public void setUser_id(String user_id) {
		this.user_id = user_id;
	}

	public Integer getRcode() {
		return rcode;
	}

	public void setRcode(Integer rcode) {
		this.rcode = rcode;
	}

	public String getRmsg() {
		return rmsg;
	}

	public void setRmsg(String rmsg) {
		this.rmsg = rmsg;
	}

	public Object getData() {
		return data;
	}

	public void setData(Object data) {
		this.data = data;
	}

}
