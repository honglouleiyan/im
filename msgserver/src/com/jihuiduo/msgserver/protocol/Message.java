package com.jihuiduo.msgserver.protocol;

import java.util.List;

import com.jihuiduo.msgserver.protocol.im.IMMessage;

/**
 * 消息(发送、推送)
 */
public class Message {
	
	private List<IMMessage> message_info;
	
	public Message() {
		
	}
	
	public Message(List<IMMessage> message_info) {
		this.message_info = message_info;
	}

	public List<IMMessage> getMessage_info() {
		return message_info;
	}

	public void setMessage_info(List<IMMessage> message_info) {
		this.message_info = message_info;
	}
	
}
